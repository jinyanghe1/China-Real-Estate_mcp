def get_city(name: str) -> dict | None:
    """根据中文名获取城市数据，支持模糊匹配。

    先尝试精确匹配，再尝试包含匹配（如 "京" 可匹配 "北京"）。
    如果有多个模糊匹配结果，返回名称最短的（最精确的）。

    Args:
        name: 城市中文名称或部分名称。

    Returns:
        包含城市名和数据的字典 ``{"city": "北京", **data}``，
        未找到时返回 ``None``。
    """
    # 精确匹配
    if name in CITY_DATA:
        return {"city": name, **CITY_DATA[name]}

    # 模糊匹配：查找名称中包含输入字符串的城市
    matches: list[str] = [
        city for city in CITY_DATA if name in city or city in name
    ]
    if not matches:
        # 尝试英文名匹配（不区分大小写）
        name_lower = name.lower()
        matches = [
            city
            for city, data in CITY_DATA.items()
            if data["name_en"].lower() == name_lower
        ]

    if matches:
        # 返回最短匹配（最精确）
        best = min(matches, key=len)
        return {"city": best, **CITY_DATA[best]}

    return None


def get_cities_by_tier(tier: int) -> list[dict]:
    """获取指定等级的所有城市。

    Args:
        tier: 城市等级（1-5）。

    Returns:
        城市数据列表，每个元素包含 ``"city"`` 键。
    """
    return [
        {"city": city, **data}
        for city, data in CITY_DATA.items()
        if data["tier"] == tier
    ]


def list_all_cities() -> list[str]:
    """返回所有城市中文名称列表。"""
    return list(CITY_DATA.keys())
